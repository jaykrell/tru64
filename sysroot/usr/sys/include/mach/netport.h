#ifndef	_MACH_NETPORT_H_
#define _MACH_NETPORT_H_

typedef unsigned long	netaddr_t;

/*
 * Network Port structure.
 */
typedef struct {
    long	np_uid_high;
    long	np_uid_low;
} np_uid_t;

typedef struct {
    netaddr_t	np_receiver;
    netaddr_t	np_owner;
    np_uid_t	np_puid;
    np_uid_t	np_sid;
} network_port_t;

#endif	/* _MACH_NETPORT_H_ */
